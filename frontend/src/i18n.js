const DICT = { "zh-CN": ZH, "zh-TW": TW, en: EN, ja: JA, ko: KO };


// Map locale codes to DICT keys
const LANG_MAP = { "zh-CN":"zh-CN", "en-US":"en", "en":"en", "zh-TW":"zh-TW", "ja-JP":"ja", "ko-KR":"ko" };
let currentLang = 'zh-CN';
const listeners = [];

function resolve(obj, path) {
  // Try flat key first (e.g., "nav.dashboard" as a single key)
  if (obj && typeof obj === 'object' && path in obj) return obj[path];
  // Try nested traversal (e.g., obj.nav.dashboard)
  const parts = path.split('.');
  let val = obj;
  for (const p of parts) { if (val && typeof val === 'object' && p in val) val = val[p]; else return undefined; }
  return typeof val === 'string' ? val : undefined;
}

export function t(key, ...args) {
  const dict = DICT[LANG_MAP[currentLang]] || DICT["zh-CN"];
  let val = resolve(dict, key) || resolve(DICT.zh, key);
  if (val === undefined) val = key;
  if (args.length > 0) val = val.replace(/\{(d+)\}/g, (m, idx) => args[idx] !== undefined ? args[idx] : m);
  return val;
}

export function setLang(lang) {
  currentLang = lang;
  listeners.forEach(fn => fn(lang));
  if (typeof localStorage !== 'undefined') localStorage.setItem('ecompany_lang', lang);
}

export function getLang() { return currentLang; }

export function onLangChange(fn) { listeners.push(fn); return () => { const i = listeners.indexOf(fn); if (i > -1) listeners.splice(i, 1); }; }

export function translateDOM(root) {
  var dictKey = LANG_MAP[currentLang] || 'zh-CN'; var dict = DICT[dictKey] || DICT['zh-CN'];
  const el = root || document;
  el.querySelectorAll('[data-i18n]').forEach(el => { const key = el.getAttribute('data-i18n'); const val = resolve(dict, key); if (val) el.textContent = val; });
  el.querySelectorAll('[data-i18n-placeholder]').forEach(el => { const key = el.getAttribute('data-i18n-placeholder'); const val = resolve(dict, key); if (val !== undefined) el.placeholder = val; });
}

onLangChange(() => { setTimeout(translateDOM, 50); });

try { const saved = typeof localStorage !== 'undefined' && localStorage.getItem('ecompany_lang'); if (saved) setLang(saved); } catch(e) {}

export default { t, setLang, getLang, onLangChange, translateDOM };
